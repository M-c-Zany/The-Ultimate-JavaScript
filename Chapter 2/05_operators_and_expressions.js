// Arithmetic Operators 
let a = 10;
let b = 4;

// Output the result of various arithmetic operations
console.log("a + b = ", a + b); // Output: a + b = 14
console.log("a - b = ", a - b); // Output: a - b = 6
console.log("a / b = ", a / b); // Output: a / b = 2.5
console.log("a ** b = ", a ** b); // Output: a ** b = 10000
console.log("a % b = ", a % b); // Output: a % b = 2
console.log("++a = ", ++a); // Output: ++a = 11
console.log("a++ = ", a++); // Output: a++ = 11
console.log("--a = ", --a); // Output: --a = 10
console.log("a-- = ", a--); // Output: a-- = 10
console.log("a = ", a); // Output: a = 9
console.log("a-- = ", a--); // Output: a-- = 9

// Assignment Operators 
let assignment = 1;

// Update 'assignment' variable using assignment operators
assignment += 5; // Output: assignment is now = 6
console.log("assignment is now = ", assignment);

assignment -= 5; // Output: assignment is now = 1
console.log("assignment is now = ", assignment);

assignment *= 5; // Output: assignment is now = 5
console.log("assignment is now = ", assignment);

assignment /= 5; // Output: assignment is now = 1
console.log("assignment is now = ", assignment);

// Comparison Operators 
let comp1 = 6;
let comp2 = 7;

// Output the result of various comparison operations
console.log("comp1 == comp2 is ", comp1 == comp2); // Output: comp1 == comp2 is false
console.log("comp1 != comp2 is ", comp1 != comp2); // Output: comp1 != comp2 is true
console.log("comp1 === comp2 is ", comp1 === comp2); // Output: comp1 === comp2 is false
console.log("comp1 !== comp2 is ", comp1 !== comp2); // Output: comp1 !== comp2 is true
console.log("comp1 > comp2 is ", comp1 > comp2); // Output: comp1 > comp2 is false

// Logical Operators
let x = 5;
let y = 6;

// Output the result of various logical operations
console.log(x < y && x == 5); // Output: true
console.log(x > y || x == 5); // Output: true
console.log(!false); // Output: true
console.log(!true); // Output: false

